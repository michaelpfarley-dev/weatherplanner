// Get unique states for grouping
export function getResortStates() {
  const states = [...new Set(SKI_RESORTS.map(r => r.state))];
  return states.sort();
}

// Filter resorts by search query
export function filterResorts(query) {
  if (!query || query.length < 2) return [];
  const q = query.toLowerCase();
  return SKI_RESORTS.filter(r =>
    r.name.toLowerCase().includes(q) ||
    r.state.toLowerCase().includes(q)
  ).slice(0, 20); // Limit results
}

// Get resorts by state
export function getResortsByState(state) {
  return SKI_RESORTS.filter(r => r.state === state);
}
